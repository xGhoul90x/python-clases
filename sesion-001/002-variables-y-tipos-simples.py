"""
Variables y tipo de datos
- Los nombres de variable no deben empezar con numeros
- Pueden empezar con guiones bajos
- A partir del segundo caracteres pueden haber numeros

String: Cadena de caracteres que se escribe entre comillas.
Integer: Datos enteros
Real: Datos reales (decimales)
Boolean: Datos booleanos que pueden tener valor de True o False
"""
